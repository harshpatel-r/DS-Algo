
//# Greatest of three numbers

// Given 3 numbers A, B and C. Find the greatest number among them.

// SOLUTION

//User function Template for javascript

/**
 * @param {number} A
 * @param {number} B
 * @param {number} C
 * @returns {number}
*/

class Solution {
    greatestOfThree(A, B, C) {
        return Math.max(A, B, C);
    }
}

