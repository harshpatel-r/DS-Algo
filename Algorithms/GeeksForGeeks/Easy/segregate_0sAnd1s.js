
//# Segregate 0s and 1s

/*
Given an array of length N consisting of only 0s and 1s in random order. 
Modify the array to segregate 0s on left side and 1s on the right side of 
the array. 
*/

// SOLUTION

//User function Template for javascript

/**
 * @param {number[]} arr
 * @param {number} n
*/

class Solution {
    segregate0and1(arr, n) {
        arr.sort()
    }
}