
//# Search an element in sorted and rotated array

/*
Given a sorted and rotated array A of N distinct elements which are rotated at some point, and 
given an element K. The task is to find the index of the given element K in array A.
*/


// SOLUTION

//User function Template for javascript
/**
 * @param {number[]} arr
 * @param {number} K
 * @returns {number}
 */
class Solution {
    Search(arr, K) {
        //code here
        return arr.indexOf(K);
    }
}
