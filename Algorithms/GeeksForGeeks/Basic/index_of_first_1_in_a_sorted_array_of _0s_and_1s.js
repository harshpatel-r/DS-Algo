
//# Index of first 1 in a sorted array of 0s and 1s

/*
Given a sorted array consisting 0s and 1s. The task is to find the index 
of first 1 in the given array. 
*/

// SOLUTION

//User function Template for javascript

/**
 * @param {number[]} arr
 * @param {number} n
 * @returns {number}
*/

class Solution {
    firstIndex(arr, n) {
        //code here
        return arr.indexOf(1);
    }
}
